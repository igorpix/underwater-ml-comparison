    plt.figure(figsize=(12, 10))
    sns.heatmap(cm, annot=True, fmt='d', cmap='Blues',
                xticklabels=classes, yticklabels=classes)
    plt.xlabel('Predicted Label') # ou apenas 'Predicted'
    plt.ylabel('True Label')      # ou apenas 'True'
    plt.title('Confusion Matrix - ResNet18 (AQUA20)')
    plt.xticks(rotation=45)
    plt.show()


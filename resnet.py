import seaborn as sns
import matplotlib.pyplot as plt
from sklearn.metrics import confusion_matrix
import numpy as np

def plot_normalized_confusion_matrix(y_true, y_pred, classes):
    # 'true' normaliza pelas linhas (Real), mostrando o Recall de cada classe
    cm = confusion_matrix(y_true, y_pred, normalize='true')

    plt.figure(figsize=(12, 10))
    sns.heatmap(cm,
                annot=True,
                fmt='.2f',          # Formato float com 2 casas decimais
                cmap='Blues',       # Mapa de cor azul
                xticklabels=classes,
                yticklabels=classes,
                vmin=0.0, vmax=1.0) # Trava a escala de cor entre 0 e 100%

    plt.title('Normalized Confusion Matrix - ResNet18 (AQUA20)')
    plt.xlabel('Predicted Label') # ou apenas 'Predicted'
    plt.ylabel('True Label')      # ou apenas 'True'
    plt.xticks(rotation=45, ha='right') # Rotação para ler melhor os nomes
    plt.tight_layout()
    plt.show()

# Chamada da função (usando as variáveis que você já tem)
